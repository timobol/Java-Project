package day3;

public class Sample302 {

	public static void main(String[] args) {
		//標準出力
		int a = 5;  //整数値
		System.out.println("a = " + a);
		//入力した値が、正の数かどうかを調べる
		if(a > 0) {
			System.out.println("aは正の数です。");  //正の数だった場合に実行
		}else{
			System.out.println("aは正の数ではありません。");  //正の数ではなかった場合に実行
		}

	}

}

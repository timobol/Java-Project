package day2;

public class prob2_1 {

	public static void main(String[] args) {
		int a= 5, b = 3;
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("a" + " + " + "b" + " = " + (a + b));
		System.out.println("a" + " - " + "b" + " = " + (a - b));
		System.out.println("a" + " * " + "b" + " = " + (a * b));
		System.out.println("a" + " / " + "b" + " = " + (a / b));
		System.out.println("a" + " % " + "b" + " = " + (a % b));
	}

}
